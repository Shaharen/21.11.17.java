
public class Exam03 {

	public static void main(String[] args) {
		// 연산자 - 단항 / 이항 / 삼항
		int num1 = 7;
		int num2 = 3;
		double num3 = 3.0;
		
		System.out.println(num1 + num2);
		System.out.println(num1 - num2);
		System.out.println(num1 * num2);
		System.out.println(num1 / num2);
		System.out.println(num1 % num2);
		System.out.println(num1 / num3);  // 실수가 포함되면 실수로 출력
		System.out.println(num1 % num3);  // 나머지는 정수까지 출력
		
		

	}

}
